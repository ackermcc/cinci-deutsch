      <div class="eng cushycms">Cincinnati&rsquo;s Heritage</div>
      <hr class="yellow">
      <div class="engContent cushycms"><p>A history of Cincinnati would be inconceivable without consideration of the population of German descent, as it has played such an important role in the social, economic, cultural and political development of the city. Already in the early 19th century, the city became one of the major centers&nbsp;of German immigration, forming the famed German Triangle along with St. Louis and Milwaukee. At that time, immigrants settled north of the Miami-Erie Canal in the area that Cincinnatians dubbed&nbsp;<a href="cincinnatiCommunity.php">&ldquo;Over-the-Rhine.&rdquo;</a> Although the last great wave was likely that of the Donauschawben in the 1950s, Germans continue to come to the city. Today, almost half of the population in the Greater Cincinnati area claims German ancestry.<br />
<br />
A countless number of sites reflecting the German heritage can be found, such as the Tyler-Davidson Fountain, which was produced in Cincinnati&rsquo;s sister city of Munich. Culture and language instruction is provided by area <a href="languageEducation.php">schools</a>, which maintain exchange programs with counterparts in Germany. The heritage is also maintained by the many German church services, radio and television programs, and annual festivals.&nbsp;<br />
<br />
The <a href="http://www.gacl.org/">German-American Citizens League (GACL)</a> serves as the umbrella organization for the German societies of the area and established the <a href="http://www.gacl.org/museum.html">German Heritage Museum</a> to illuminate the rich heritage of the region. Cincinnati justifiably enjoys the well-deserved reputation of being considered the unofficial capital city of German-American culture in the United States.<br />
