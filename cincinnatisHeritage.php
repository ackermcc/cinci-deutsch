      <div title="eng title" class="eng cushycms">Cincinnati’s Heritage</div>
      <hr class="yellow">
      <div title="eng content" class="cushycms">
        <p>A history of Cincinnati would be inconceivable without consideration of the population of German descent, as it has played 
          such an important role in the social, economic, cultural and political development of the city. Already in the early 19th 
          century, Cincinnati became one of the major centers of German immigration, forming the famed German Triangle along with St. 
          Louis and Milwaukee. At that time, German immigrants settled north of the Miami-Erie Canal in the area that Cincinnatians 
          called “Over-the-Rhine.” Today, almost half of the population in the Greater Cincinnati area claims German ancestry.</p>
        <p>The first Germans arrived after the American Revolution, but it was particularly Martin Baum (born 1765 in Hagenau in Alsace) 
          who contributed greatly to the growth and development of the city and the German element. He recruited German immigrants as 
          workers for the many businesses that he established and is actually considered the “Father of the German immigration to 
          Cincinnati.” In 1802, David Ziegler (born 1748 in Heidelberg) was elected as the first mayor of the city. In the course of 
          the 19th century, wave after wave of German immigrants arrived. Although the last great wave of German immigrants was likely 
          that of the Donauschawben in the 1950s, Germans continue to come to the city to the present day.</p>
        <p>The very symbol of the city, the Tyler-Davidson Fountain, was produced in Cincinnati’s sister city of Munich in the Royal 
          Bronze Foundry. A countless number of sites reflecting the German heritage can be found, such as the Roebling Suspension Bridge. 
          German instruction is provided by area schools and universities, which also maintain exchange programs with counterparts in 
          Germany. German church services can be found in Cincinnati, as well as German-American radio and television programs, such as 
          Deutsche Welle.</p>
        <p>German festivals take place here annually, such as: Maifest, Schützenfest, Oktoberfest and Christkindlmarkt. The German-American 
          Citizens League, which was founded in 1895, serves as the umbrella organization for the German societies of the area. In 2000, 
          t established the German Heritage Museum to illuminate the German heritage of the region. Cincinnati justifiably enjoys the 
          ell-deserved reputation of being considered the unofficial capital city of German-American culture in the USA.</p>
        <p>Article written by </br>
          Dr. Don Heinrich Tolzmann </br>
          GACL President</p>
