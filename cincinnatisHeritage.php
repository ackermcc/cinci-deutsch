      <div title="ger content" class="ger cushycms">
        <p>Undenkbar wäre eine Gechichte Cincinnatis ohne Berücksichtigung der deutschstämmigen Bevőlkerung, weil sie eine bedeutende 
          Rolle in der sozialen, wirtschaftlichen, kulturellen und politschen Entwicklung der Stadt gespielt hat. Schon früh im 19. 
          Jahrhundert wurde Cincinnati eines der Zentren der deutschen Einwanderung und bildete mit St. Louis und Milwaukee das 
          sogannte Deutsche Dreieck. Damals siedelten sich deutsche Einwanderer nőrdlich des Miami-Erie Kanals im Gebiet an, das 
          von Cincinnatiern als “Über’m Rhein” getauft wurde. Heutzutage soll fast die Hälfte der Bevőlkerung im Raum Cincinnati 
          deutscher Abstammung sein.</p>
        <p>Nach der Amerikanischen Revolution kamen die ersten Deutschen in Cincinnati an. Besonders war es Martin Baum (1765 in Hagenau 
          im Elsass geboren), der zum mächtigen Aufschwung der Stadt sowie des deutschen Elements beitrug. Er warb deutsche Einwanderer 
          als Arbeiter für die von ihm gegründeten Firmen und gilt eigentlich als ”Vater der deutschen Einwanderung nach Cincinnati.” 
          1802 wurde David Ziegler (1748 in Heidelberg geboren) zum ersten Bürgermeister der Stadt gewählt. Im Laufe des 19. Jahrhunderts
           kam eine Welle deutscher Einwanderer nach der anderen hier an. Obwohl die letzte grosse Welle wohl die der Donauschwaben in 
           den 1950er Jahren war, kommen immer noch bis auf heute Deutsche hier an.</p>
        <p>Das Wahrzeichen der Stadt, der Tyler-Davidson Brunnen, wurde in Cincinnatis Partnerstadt München in der Kőniglichen Erzgiesserei
         gegossen. Zahlreiche Beispiele des deutschen Erbes findet man ueberall, wie z.B. die Roebling-Hängebrücke. Deutschunterricht gibt 
         es in den Schulen und Universitäten, die Austauschprogramme mit Partnern in Deutschland veranstaltet haben. In Cincinnati findet 
         man deutschsprachige Kirchen sowie deutsch-amerikanische Radioprogramme.</p>
        <p>Deutsche Welle kann man auch per Kabel empfangen. Deutsche Feste finden hier alljährlich statt, darunter: Maifest, Schützenfest,
         Oktoberfest, und Christkindlmarkt. Die Deutsch-Amerikanische Büger-Liga, die 1895 gegründet wurde, gilt als Dachverband deutscher
          Vereine im Raum Cincinnati.  Von ihr wurde im Jahre 2000 ein Deutsches Museum ins Leben gerufen, um das deutsche Erbe der 
          Gegend zu beleuchten. Mit Recht geniesst Cincinnati schon den wohlverdienten Ruf, als die inoffizielle Hauptstadt 
          deutsch-amerikanischer Kultur in den USA angesehen zu werden.</p>
