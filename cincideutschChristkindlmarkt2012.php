      <div class="gerTitle cushycms">Cincideutsch Christkindlmarkt 2012</div>
      <hr class="red">
      <div class="ger cushycms">Cincideutsch pr&auml;sentierte den ersten authentischen deutschen Christkindlmarkt in Downtown Cincinnati&rsquo;s Fountain Square. Der Markt fand am Wochenende nach Thanksgiving statt und lokale Gesch&auml;fte und Organisationen hatten die M&ouml;glichkeit, ihre Produkte zu verkaufen. Vom Weihnachtsschmuck &uuml;ber importiere deutsche Artikel bis deutsche S&uuml;ssigkeiten war alles vertreten.<br />
<br />
Die folgenden Verk&auml;ufer hatten am Christkindlmarkt ihre Produkte angeboten: Ultimate Almonds, <a href="http://www.cin-deescrafts.com/">Cin-dee&rsquo;s Crafts</a>, <a href="http://www.germaniasociety.com/site2007/main.html">Germania Society</a>, <a href="http://www.munichcincinnati.org/">Munich Sister Cities</a>, <a href="https://www.rookwood.com/">Rookwood Pottery</a>, <a href="http://www.servatiipastryshop.com/Locations.aspx">Servatii&rsquo;s Pastry Shop &amp; Deli</a> und St. Joseph katholische Schule.<br />
<br />
In Kooperation mit der Senior Designs Klasse der <a href="http://www.saintursula.org/arts/Design/">Saint Ursula Akademie</a>&nbsp;&nbsp;arbeitete Cincideutsch mit den Studenten zusammen, um Tassen mit verschiedenen Gestaltungen zu entwerfen. Die Senior-Studenten informierten sich zuerst &uuml;ber die moderne deutsche Kunst und danach recherchierten sie &uuml;ber einen Designer der Bauhaus Design Schule. Inspiriert vom Design der ausgew&auml;hlte Designer stellten die Studenten dann die Tassen her, auf denen in abstrakter Weise deutsche W&ouml;rter aufgedruckt wurden. Es wurden spezielle deutsche W&ouml;rter verwendet, die nicht oder nur schwer auf Englisch &uuml;bersetzt werden k&ouml;nnen.<br />
<br />
<strong>Um einen dieser wunderbaren Tassen</strong> der Saint Ursula Akedemie zu bestellen, kann eine E-Mail an <a href="mailto:info@cincideutsch.com?subject=SUA%20Mug%20Order">info@cincideutsch.com</a> gesendet werden. Alle verf&uuml;gbaren Tassen sind im <a href="cincideutschChristkindlmarkt2012_16_1808097046.pdf">PDF Dokument</a> ersichtlich.</div>
