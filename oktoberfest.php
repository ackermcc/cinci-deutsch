        <p>Autumn is the busiest time of year for the German community in Cincinnati. From the last week of August through the first week 
          of October, Oktoberfest celebrations can be found every weekend throughout the city. In fact, Cincinnati has more Oktoberfests 
          than any other city in the United States and has acquired the nickname Oktoberfest City, USA. </p>
        <p>The season begins with the Germania Society Oktoberfest at Germania Park. It was the first Oktoberfest in Cincinnati, introduced
         in 1971 and originally held in Cheviot. A few weeks later, downtown Cincinnati touts one of the largest Oktoberfests in the world, 
         only second in size to the original in Munich. The Zinzinnati Oktoberfest’s opening ceremonies coincide with festivities in Munich. 
         Each year, over half a million people make their way to Zinzinnati to get a taste of German culture by sampling pretzels, wurst, 
         sauerkraut balls, and Oktoberfest beers. Finally, the Donauschwaben Society ends the season at Dounauschwaben Park.</p>
        <p>Oktoberfest originally began in Munich, Cincinnati’s Sister-City, as the wedding celebration of  the Bavarian Crown Prince Ludwig
         (King Ludwig I) to Princess Therese of Saxe-Hildburghausen, which is now the Free State of Thüringen, located north of present-day
          Bavaria. The primary feature of the event was horse racing. So successful was this event, that the meadows in which the races took 
          place became known as the Theresa Meadows, or d’Weis’n, in honor of the Crown Princess. In following years, horse racing became part 
          of the opening ceremonies and the commemorative event took more of a festival-like atmosphere. </p>
